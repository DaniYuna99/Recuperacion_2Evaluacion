package ejercicios;

public class Ejercicio14 {

	public static void main(String[] args) {

		/*14. Muestra los números múltiplos de 5 de 0 
		 * a 100 utilizando un bucle while.*/
		
		int contador = 0;
		
		while (contador <= 100) {
			System.out.println(contador);
			contador = contador + 5;
		}
	}

}
